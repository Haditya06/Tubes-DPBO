    // LOGIKA: Save Examination ke Database
    // ==========================================
    private void saveExamination(String diagnosis, String medicalAction, double serviceCost) throws SQLException {
        Connection conn = DatabaseConnection.getConnection();
        conn.setAutoCommit(false);

        try {
            int idHewan = 0;
            String sqlGetHewan = "SELECT id_hewan FROM reservasi WHERE id_reservasi = ?";
            PreparedStatement psGetHewan = conn.prepareStatement(sqlGetHewan);
            psGetHewan.setInt(1, idReservasi);
            ResultSet rsGetHewan = psGetHewan.executeQuery();
            if (rsGetHewan.next()) {
                idHewan = rsGetHewan.getInt("id_hewan");
                System.out.println("[DEBUG] ID Hewan: " + idHewan);
            }

            String sqlPemeriksaan = "INSERT INTO pemeriksaan (id_reservasi, id_dokter, diagnosis, tindakan, biaya_layanan, tanggal_periksa) "
                    + "VALUES (?, 1, ?, ?, ?, NOW())";

            PreparedStatement psPemeriksaan = conn.prepareStatement(sqlPemeriksaan, Statement.RETURN_GENERATED_KEYS);
            psPemeriksaan.setInt(1, idReservasi);
            psPemeriksaan.setString(2, diagnosis);
            psPemeriksaan.setString(3, medicalAction);
            psPemeriksaan.setDouble(4, serviceCost);
            psPemeriksaan.executeUpdate();

            ResultSet rs = psPemeriksaan.getGeneratedKeys();
            int idPemeriksaan = 0;
            if (rs.next()) {
                idPemeriksaan = rs.getInt(1);
            }

            System.out.println("[DEBUG] ID Pemeriksaan baru: " + idPemeriksaan);

            String sqlRekamMedis = "INSERT INTO rekam_medis (id_hewan, id_pemeriksaan, riwayat_penyakit) "
                    + "VALUES (?, ?, ?)";
            PreparedStatement psRekam = conn.prepareStatement(sqlRekamMedis);
            psRekam.setInt(1, idHewan);
            psRekam.setInt(2, idPemeriksaan);
            psRekam.setString(3, diagnosis);
            psRekam.executeUpdate();

            System.out.println("[DEBUG] Rekam medis disimpan untuk hewan ID: " + idHewan);

            String sqlDetail = "INSERT INTO detail_transaksi (id_pemeriksaan, id_obat, qty, harga_satuan, subtotal) "
                    + "VALUES (?, ?, ?, ?, ?)";
            PreparedStatement psDetail = conn.prepareStatement(sqlDetail);

            for (DetailTransaksi detail : daftarResep) {
                String namaItem = detail.getNamaItem();
                int qty = detail.getQty();
                double harga = detail.getHargaSatuan();
                double subtotal = detail.hitungSubtotal();

                String sqlGetIdObat = "SELECT id_obat FROM obat WHERE nama_obat = ?";
                PreparedStatement psGetId = conn.prepareStatement(sqlGetIdObat);
                psGetId.setString(1, namaItem);
                ResultSet rsId = psGetId.executeQuery();

                int idObatInt = 0;
                if (rsId.next()) {
                    idObatInt = rsId.getInt("id_obat");
                }

                if (idObatInt > 0) {
                    psDetail.setInt(1, idPemeriksaan);
                    psDetail.setInt(2, idObatInt);
                    psDetail.setInt(3, qty);
                    psDetail.setDouble(4, harga);
                    psDetail.setDouble(5, subtotal);
                    psDetail.addBatch();

                    String sqlUpdateStok = "UPDATE obat SET stok = stok - ? WHERE id_obat = ?";
                    PreparedStatement psUpdateStok = conn.prepareStatement(sqlUpdateStok);
                    psUpdateStok.setInt(1, qty);
                    psUpdateStok.setInt(2, idObatInt);
                    psUpdateStok.executeUpdate();
                }
            }

            psDetail.executeBatch();

            String sqlUpdateStatus = "UPDATE reservasi SET status_reservasi = 'SELESAI' WHERE id_reservasi = ?";
            PreparedStatement psUpdateStatus = conn.prepareStatement(sqlUpdateStatus);
            psUpdateStatus.setInt(1, idReservasi);
            psUpdateStatus.executeUpdate();

            conn.commit();
            System.out.println("[DEBUG] Transaction committed successfully!");

        } catch (SQLException e) {
            conn.rollback();
            System.err.println("[ERROR] Transaction rolled back: " + e.getMessage());
            throw e;
        } finally {
            conn.setAutoCommit(true);
        }
    }
    
