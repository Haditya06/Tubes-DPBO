    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String selectedItem = (String) jComboBox1.getSelectedItem();

        if (selectedItem == null || selectedItem.equals("-- Pilih Obat --")) {
            JOptionPane.showMessageDialog(this, "Pilih obat terlebih dahulu!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int qty = Integer.parseInt(tfQuantity.getText().trim());
            if (qty <= 0) {
                JOptionPane.showMessageDialog(this, "Quantity harus lebih dari 0!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Parse data dari dropdown
            String[] parts = selectedItem.split("\\|");
            int idObat = Integer.parseInt(parts[0]);
            String[] info = parts[1].split(" - ");
            String namaObat = info[0];

            // Ambil harga dari database
            Connection conn = DatabaseConnection.getConnection();
            String sql = "SELECT harga, stok FROM obat WHERE id_obat = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idObat);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                double harga = rs.getDouble("harga");
                int stokTersedia = rs.getInt("stok");

                if (qty > stokTersedia) {
                    JOptionPane.showMessageDialog(this, "Stok tidak mencukupi! Tersedia: " + stokTersedia, "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                double subtotal = harga * qty;

                // Tambah ke tabel
                DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                model.addRow(new Object[]{namaObat, qty, formatRupiah(subtotal), "Delete"});

                // ✅ PENTING: Buat DetailTransaksi dengan constructor yang benar
                DetailTransaksi detail = new DetailTransaksi(
                        "DET-" + System.currentTimeMillis(), // ID dummy
                        namaObat, // Nama item
                        qty, // Quantity
                        harga // Harga satuan
                );
                daftarResep.add(detail);

                // Update total
                totalObat += subtotal;
                updateCostCalculation();

                // Clear quantity
                tfQuantity.setText("1");

                JOptionPane.showMessageDialog(this, "Obat berhasil ditambahkan!");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan quantity yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

