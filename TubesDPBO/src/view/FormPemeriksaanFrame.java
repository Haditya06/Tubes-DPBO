    // ✅ BARU: Setup Listener untuk Tombol Delete
    // ==========================================
    private void setupDeleteListener() {
        jTable2.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = jTable2.rowAtPoint(evt.getPoint());
                int col = jTable2.columnAtPoint(evt.getPoint());

                if (col == 3 && row >= 0) { // Kolom "Del"
                    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
                    String namaObat = model.getValueAt(row, 0).toString();
                    int qty = Integer.parseInt(model.getValueAt(row, 1).toString());

                    // Hapus dari list
                    daftarResep.removeIf(d -> {
                        Obat o = (Obat) d.getObat();
                        return o != null && o.getNamaObat().equals(namaObat) && d.getQty() == qty;
                    });

                    // Hapus dari tabel
                    model.removeRow(row);

                    // Recalculate total
                    recalculateTotal();
                }
            }
        });
    }

