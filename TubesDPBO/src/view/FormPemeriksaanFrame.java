    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {
        int row = jTable2.rowAtPoint(evt.getPoint());
        int col = jTable2.columnAtPoint(evt.getPoint());

        if (col == 3) { // Kolom "Del"
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            String namaObat = model.getValueAt(row, 0).toString();
            int qty = Integer.parseInt(model.getValueAt(row, 1).toString());

            // Hapus dari list
            daftarResep.removeIf(d -> ((Obat) d.getObat()).getNamaObat().equals(namaObat) && d.getQty() == qty);

            // Hapus dari tabel
            model.removeRow(row);

            // Recalculate total
            recalculateTotal();
        }
    }
    
    // ==========================================
    // HELPER: Recalculate Total Obat
    // ==========================================
    private void recalculateTotal() {
        totalObat = 0;
        for (DetailTransaksi detail : daftarResep) {
            totalObat += detail.hitungSubtotal();
        }
        updateCostCalculation();
    }
    
    // ==========================================
    // HELPER: Update Cost Calculation Display
    // ==========================================
    private void updateCostCalculation() {
        try {
            double serviceCost = Double.parseDouble(tfServiceCost.getText().trim());
            tfServisCostPerhitungan.setText(formatRupiah(serviceCost));
            tfMedicines.setText(formatRupiah(totalObat));
            double total = serviceCost + totalObat;
            tfTotal.setText(formatRupiah(total));
        } catch (NumberFormatException e) {
            tfServisCostPerhitungan.setText("Rp 0");
            tfMedicines.setText(formatRupiah(totalObat));
            tfTotal.setText(formatRupiah(totalObat));
        }
    }
