    private void loadPatientData() {
        try {
            Connection conn = DatabaseConnection.getConnection();
            String sql = "SELECT r.id_reservasi, h.nama_hewan, h.jenis, h.umur, "
                    + "u.nama_lengkap, r.tanggal, l.nama_layanan "
                    + "FROM reservasi r "
                    + "JOIN hewan h ON r.id_hewan = h.id_hewan "
                    + "JOIN customer c ON r.id_customer = c.id_customer "
                    + "JOIN users u ON c.id_user = u.id_user "
                    + "LEFT JOIN pemeriksaan p ON r.id_reservasi = p.id_reservasi "
                    + "LEFT JOIN layanan l ON p.id_layanan = l.id_layanan "
                    + "WHERE r.id_reservasi = ?";

            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idReservasi);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                tfUmurPet.setText(rs.getString("nama_hewan") + " (" + rs.getString("jenis") + ", " + rs.getInt("umur") + " yr)");
                tfReservationID1.setText("R-" + rs.getInt("id_reservasi"));
                tfOwner.setText(rs.getString("nama_lengkap"));
                tfService.setText(rs.getString("nama_layanan") != null ? rs.getString("nama_layanan") : "General Checkup");
                tfDate.setText(rs.getString("tanggal"));

                // Set non-editable
                tfUmurPet.setEditable(false);
                tfReservationID1.setEditable(false);
                tfOwner.setEditable(false);
                tfService.setEditable(false);
                tfDate.setEditable(false);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading patient data: " + e.getMessage());
        }
    }
    
    // ==========================================
    // LOGIKA: Load Obat ke Dropdown
    // ==========================================
    private void loadMedicinesToDropdown() {
        try {
            Connection conn = DatabaseConnection.getConnection();
            String sql = "SELECT id_obat, nama_obat, harga, stok FROM obat WHERE stok > 0 ORDER BY nama_obat";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            jComboBox1.removeAllItems();
            jComboBox1.addItem("-- Pilih Obat --");

            while (rs.next()) {
                int idObat = rs.getInt("id_obat");
                String namaObat = rs.getString("nama_obat");
                double harga = rs.getDouble("harga");
                int stok = rs.getInt("stok");
                String item = idObat + "|" + namaObat + " - Rp " + formatRupiah(harga) + " (Stok: " + stok + ")";
                jComboBox1.addItem(item);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading medicines: " + e.getMessage());
        }
    }
    
