    private void loadStatistics() {
        try {
            Connection conn = DatabaseConnection.getConnection();

            String sql = "SELECT "
                    + "SUM(CASE WHEN status_reservasi IN ('CHECK_IN', 'ASSIGNED') THEN 1 ELSE 0 END) as waiting, "
                    + "SUM(CASE WHEN status_reservasi = 'DIPERIKSA' THEN 1 ELSE 0 END) as examination, "
                    + "SUM(CASE WHEN status_reservasi IN ('SELESAI', 'LUNAS') AND DATE(tanggal) = CURDATE() THEN 1 ELSE 0 END) as completed "
                    + "FROM reservasi";

            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                tfWaitingPatients.setText(String.valueOf(rs.getInt("waiting")));
                tfExamination.setText(String.valueOf(rs.getInt("examination")));
                tfCompleateToday.setText(String.valueOf(rs.getInt("completed")));
            }

            // Set non-editable
            tfWaitingPatients.setEditable(false);
            tfExamination.setEditable(false);
            tfCompleateToday.setEditable(false);

        } catch (SQLException e) {
            System.err.println("[DokterDashboard] Error load statistics: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    private void loadPatientQueue() {
        try {
            Connection conn = DatabaseConnection.getConnection();

            // Query: Ambil reservasi yang statusnya siap diperiksa
            String sql = "SELECT r.id_reservasi, u.nama_lengkap, h.nama_hewan, h.jenis, "
                    + "COALESCE(l.nama_layanan, 'General Checkup') as layanan, "
                    + "DATE(r.tanggal) as tanggal, TIME(r.tanggal) as waktu, r.status_reservasi "
                    + "FROM reservasi r "
                    + "JOIN hewan h ON r.id_hewan = h.id_hewan "
                    + "JOIN customer c ON r.id_customer = c.id_customer "
                    + "JOIN users u ON c.id_user = u.id_user "
                    + "LEFT JOIN pemeriksaan p ON r.id_reservasi = p.id_reservasi "
                    + "LEFT JOIN layanan l ON p.id_layanan = l.id_layanan "
                    + "WHERE r.status_reservasi IN ('CHECK_IN', 'ASSIGNED', 'DIPERIKSA') "
                    + "ORDER BY r.tanggal ASC";

            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel model = new DefaultTableModel(
                    new String[]{"Res. ID", "Owner", "Pet Name", "Type", "Service", "Date", "Time", "Status"}, 0
            );

            while (rs.next()) {
                model.addRow(new Object[]{
                    "R-" + rs.getInt("id_reservasi"),
                    rs.getString("nama_lengkap"),
                    rs.getString("nama_hewan"),
                    rs.getString("jenis"),
                    rs.getString("layanan"),
                    rs.getString("tanggal"),
                    rs.getString("waktu"),
                    rs.getString("status_reservasi")
                });
            }

            jTable1.setModel(model);

        } catch (SQLException e) {
            System.err.println("[DokterDashboard] Error load queue: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }
    

