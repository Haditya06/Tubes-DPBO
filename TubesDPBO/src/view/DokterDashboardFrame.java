    private void btnStartExaminationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartExaminationActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih pasien yang akan diperiksa terlebih dahulu!",
                    "Warning",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Ambil ID Reservasi dari tabel (format "R-1" -> ubah jadi int 1)
        String resId = jTable1.getValueAt(selectedRow, 0).toString();
        int idReservasi = Integer.parseInt(resId.replace("R-", ""));

        // Update status ke DIPERIKSA di database
        try {
            Connection conn = DatabaseConnection.getConnection();
            String sql = "UPDATE reservasi SET status_reservasi = 'DIPERIKSA' WHERE id_reservasi = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idReservasi);
            ps.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }

        // Buka FormPemeriksaanFrame
        FormPemeriksaanFrame form = new FormPemeriksaanFrame(idReservasi);

        // Efek pop-up (seperti yang kita lakukan di modul Staff)
        form.setLocationRelativeTo(this);
        form.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                loadStatistics();      // Refresh statistik
                loadPatientQueue();    // Refresh tabel antrean
            }
        });

        form.setVisible(true);
    }//GEN-LAST:event_btnStartExaminationActionPerformed

